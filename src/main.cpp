public:
    struct Entity
    {
        glm::vec3 position;
        float angle;
        size_t meshIndex;
        glm::vec3 color;
    };

    class Tank
    {
        enum class Steer
        {
            neutral,
            left,
            right
        };

    public:
        Tank(Entity *entity) : entity(entity) {}

        void steerLeft()
        {
            steering = Steer::left;
        }
        void steerRight()
        {
            steering = Steer::right;
        }
        void releaseSteering()
        {
            steering = Steer::neutral;
        }

        const glm::vec3 position() const
        {
            return entity->position;
        }

        void advance()
        {
            advancing = true;
        }

        void halt()
        {
            advancing = false;
        }

        void update()
        {
            static float steerRate = 0.05;
            static float speed = 0.1;
            switch (steering)
            {
            case Steer::left:
                entity->angle += steerRate;
                break;
            case Steer::right:
                entity->angle -= steerRate;
                break;
            case Steer::neutral:
                break;
            }

            if (advancing)
            {
                glm::vec3 direction = glm::rotateY(glm::vec3{0.0, 0.0, -1.0}, entity->angle);
                entity->position += direction * speed;
            }
        }

    private:
        Entity *entity;
        Steer steering = Steer::neutral;
        bool advancing = false;
    };

public:
    void onKeyDown(int key) override
    {
        std::cout << "KEY DOWN: " << key << std::endl;
        if (players.size() < 2 || players[0] == nullptr || players[1] == nullptr)
            return;
        if (key == GLFW_KEY_A)
        {
            players[0]->steerLeft();
        }
        else if (key == GLFW_KEY_D)
        {
            players[0]->steerRight();
        }
        else if (key == GLFW_KEY_W)
        {
            players[0]->advance();
        }

        if (key == GLFW_KEY_LEFT)
        {
            players[1]->steerLeft();
        }
        else if (key == GLFW_KEY_RIGHT)
        {
            players[1]->steerRight();
        }
        else if (key == GLFW_KEY_UP)
        {
            players[1]->advance();
        }
    }

    void onKeyUp(int key) override
    {
        if (players.size() < 2 || players[0] == nullptr || players[1] == nullptr)
            return;

        if (key == GLFW_KEY_A || key == GLFW_KEY_D)
        {
            players[0]->releaseSteering();
        }
        else if (key == GLFW_KEY_W)
        {
            players[0]->halt();
        }
        if (key == GLFW_KEY_LEFT || key == GLFW_KEY_RIGHT)
        {
            players[1]->releaseSteering();
        }
        else if (key == GLFW_KEY_UP)
        {
            players[1]->halt();
        }
    }

